hs.window.animationDuration = 0

local restorePositions = {}

function saveRestorePosition(win)
  if not restorePositions[win:id()] then
    print("Saved restore position", win:id())
    restorePositions[win:id()] = win:frame()
  end
end

function saveAndSetFrame(win, frame)
  saveRestorePosition(win)
  win:setFrame(frame)
end

function hs.window.restore(win)
  local f = win:frame()
  local id = win:id()
  local pos = restorePositions[id]
  if pos then
    win:setFrame(pos)
    restorePositions[id] = nil
  end
end

